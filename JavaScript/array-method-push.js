// definition
/*In JavaScript, the push method is used to add one or more 
elements to the end of an array. This method modifies the 
original array and returns the new length of the array. 
Here is the syntax and an example of how to use the push*/

// Syntax
// array.push(element1, ..., elementN)

let fruits = ["apple", "banana"];
fruits.push("orange", "mango","tomato");

console.log(fruits); // Output: ["apple", "banana", "orange", "mango"]
console.log(fruits.length); // Output: 5
