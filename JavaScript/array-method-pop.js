// definition
/* The pop() method in JavaScript is used to remove the last element
 from an array and returns that element. This method changes the length of the array.*/
//Syntax
// array.pop();

let fruits = ["Apple", "Banana", "Cherry"];
let lastFruit = fruits.pop();

console.log(fruits);    // Output: ["Apple", "Banana"]
console.log(lastFruit); // Output: "Cherry"
